package upi.edu.hagaibrayens.homescreen;

public class User {

    public String name, username, alamat, hp, tanggal;

    public User(){

    }

    public User(String name, String username, String alamat, String hp, String tanggal) {
        this.name = name;
        this.username = username;
        this.alamat = alamat;
        this.hp = hp;
        this.tanggal = tanggal;
    }
}
